    public ListaClientesDialog() {
        setContentPane(contentPane);
        setModal(true);

        getRootPane().registerKeyboardAction(
                e -> {
                    if (buttonOK != null) buttonOK.doClick();
                },
                KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, 0),
                JComponent.WHEN_IN_FOCUSED_WINDOW
        );

        buttonOK.addActionListener(e -> onOK());
        buttonCancel.addActionListener(e -> onCancel());

        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
        addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                onCancel();
            }
        });

        contentPane.registerKeyboardAction(
                e -> onCancel(),
                KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0),
                JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT
        );

        table1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2 && table1.getSelectedRow() != -1) {
                    onOK();
                }
            }
        });
    }

    public void configurar(String titulo, List<Cliente> clientes) {
        this.clientes = clientes;
        setTitle(titulo);

        DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{"ID", "Nombre", "Apellido", "Email"},
                0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Cliente c : clientes) {
            modelo.addRow(new Object[]{
                    c.getId(),
                    c.getNombre(),
                    c.getApellido(),
                    c.getEmail()
            });
        }

        table1.setModel(modelo);
        table1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void onOK() {
        int fila = table1.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Ten√©s que seleccionar un cliente.",
                    "Seleccionar cliente",
                    JOptionPane.INFORMATION_MESSAGE
            );
            return;
        }

        clienteSeleccionado = clientes.get(fila);
        dispose();
    }

    private void onCancel() {
        clienteSeleccionado = null;
        dispose();
    }

    public Cliente mostrar() {
        pack();
        setLocationRelativeTo(null);
        setVisible(true);
        return clienteSeleccionado;
    }

    public static Cliente seleccionarCliente(String titulo, List<Cliente> clientes) {
        ListaClientesDialog dialog = new ListaClientesDialog();
        dialog.configurar(titulo, clientes);
        return dialog.mostrar();
    }

